    private void importButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_importButtonActionPerformed
         // TODO add your handling code here:
        try 
        {
            if(fileName.getText().isEmpty())
                throw new ExceptionHandler("Please enter file name !!");
            
            File file= new File(fileName.getText().trim());
            Scanner scan=new Scanner(file);
            
            int v=scan.nextInt();
            scan.nextLine();
            while(v-- > 0)
            {
                String inp[]=scan.nextLine().split(" ");
                String name=inp[0];
                int x=Integer.parseInt(inp[1]);
                int y=Integer.parseInt(inp[2]);

                g.addVertex(name, x, y);
            }
            g.printPointsMap();
            int e=scan.nextInt();
            scan.nextLine();
            while(e-- > 0)
            {
                String inp[]=scan.nextLine().split(" ");
                String from=inp[0];
                String to=inp[1];
                int wt=Integer.parseInt(inp[2]);
                g.addEdge(from, to, wt);
            }
            
            if(scan.hasNextLine())
            {
                throw new ExceptionHandler("Invalid input");
            }
            
            g.printEdgeMap();
            
            JOptionPane.showMessageDialog(null,"Import successfull !!");
            
        } 
        catch (Exception e) 
        {
            JOptionPane.showMessageDialog(null,"Invalid input");
        }
        
    }//GEN-LAST:event_importButtonActionPerformed

    private void exportButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_exportButtonActionPerformed
        // TODO add your handling code here:
        try 
        {
            
            File f=new File("output.txt");
            f.createNewFile();
            
            
            FileWriter file=new FileWriter("output.txt");
            int v=g.pointsMap.size();
            file.write(""+v+"\n");
            
            for(Map.Entry<String,List<Integer>> entry : g.pointsMap.entrySet())
            {
                String name=entry.getKey();
                int x=entry.getValue().get(0);
                int y=entry.getValue().get(1);
                
                file.write(name + " " + x + " " + y + "\n");
            }
            
            int e=0;
            for (Map.Entry<String, SortedMap<String, Integer>> outerEntry : g.graph.entrySet()) 
            {

                SortedMap<String, Integer> innerGraph = outerEntry.getValue();
           
                for (Map.Entry<String, Integer> innerEntry : innerGraph.entrySet()) 
                {
                    e++;
                }

            }
            file.write(""+e+"\n");
            
            for (Map.Entry<String, SortedMap<String, Integer>> outerEntry : g.graph.entrySet()) 
            {

                String from = outerEntry.getKey();
                SortedMap<String, Integer> innerGraph = outerEntry.getValue();
           
                for (Map.Entry<String, Integer> innerEntry : innerGraph.entrySet()) 
                {
                    String to = innerEntry.getKey();
                    int wt = innerEntry.getValue();
                    
                    file.write(from + " " + to + " " + wt + "\n");
                }

            }
            JOptionPane.showMessageDialog(null,"Export Successfull to output.txt !!");
            file.close();
        } 
        catch (Exception e) 
        {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_exportButtonActionPerformed

