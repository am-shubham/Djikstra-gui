    private void deleteVerButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteVerButtonActionPerformed
        // TODO add your handling code here:
        String name;
        try 
        {
            if(deleteVerName.getText().isEmpty())
                throw new ExceptionHandler("Please enter all fields !!");
          
            name=deleteVerName.getText().trim();
        
            if(!g.pointsMap.containsKey(name))
                throw new ExceptionHandler("Vertex with given name does not exist !!");
            
            //remove vertex from point map
            g.pointsMap.remove(name);
            //remove all edges going from "Name"
            g.edgeCount-=g.graph.get(name).size();
            g.graph.remove(name);
            g.vertexCount--;
            
            for (Map.Entry<String, SortedMap<String, Integer>> outerEntry : g.graph.entrySet()) 
            {

                String from = outerEntry.getKey();
                //remove all vertices going to "Name"
                if(g.graph.containsKey(from) && g.graph.get(from).containsKey(name));
                {
                    g.edgeCount--;
                    g.graph.get(from).remove(name);
                }

            }
            JOptionPane.showMessageDialog(null,"Vertex : "+name+" removed");
            
            System.out.println("New points map is -");
            g.printPointsMap();
            g.printEdgeMap();
            
        } 
        catch(Exception e) 
        {
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_deleteVerButtonActionPerformed

